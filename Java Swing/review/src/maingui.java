	private void showProduct() {
		dfProductModel.setRowCount(0);
		for (Product prd : productData) {
			dfProductModel.addRow(new Object[] {
				prd.getId(),
				prd.getProductName(),
				prd.getCatID(),
				prd.getPrice(),
				prd.getDescription()
			});
		}
	}

	private void showCategory() {
		dfCategoryModel.setRowCount(0);
		for (Category cat : catData) {
			dfCategoryModel.addRow(new Object[] { cat.getId(), cat.getCatName() });
		}
	}

