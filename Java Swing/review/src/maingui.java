    private void btnProductAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProductAddActionPerformed
        String prdName = txtProductName.getText();
        int categoryID = cmbProductCategory.getSelectedIndex();
        int price = Integer.parseInt(txtProductPrice.getText());
        String desc = txtProductDescription.getText();

        Product addData = new Product(prdName, categoryID, price, desc);

        ProductModifier.addProduct(addData);

        showProduct();

    }//GEN-LAST:event_btnProductAddActionPerformed

    private void btnProductEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProductEditActionPerformed
        if (productIndex != -1) {
            productData.get(productIndex).setProductName(txtProductName.getText());
            productData.get(productIndex).setCatID(cmbProductCategory.getSelectedIndex());
            productData.get(productIndex).setPrice(Integer.parseInt(txtProductPrice.getText()));
            productData.get(productIndex).setDescription(txtProductDescription.getText());

            ProductModifier.editProduct(productData.get(productIndex));

            productIndex = -1;

            showProduct();
        } else {
            return;
        }
    }//GEN-LAST:event_btnProductEditActionPerformed

    private void btnProductDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProductDeleteActionPerformed
        if (productIndex != -1) {
			int opt = JOptionPane.showConfirmDialog(rootPane, "Bạn muốn xóa sản phẩm đã chọn?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

			if (opt != 1) {
				ProductModifier.deleteProduct(productIndex);
				showProduct();
			}
		} else {
			return;
		}
    }//GEN-LAST:event_btnProductDeleteActionPerformed

    private void btnProductSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProductSearchActionPerformed
        String searchTerm = JOptionPane.showInputDialog(rootPane, "Nhập sản phẩm muốn tìm kiếm:", "Tìm sản phẩm", JOptionPane.QUESTION_MESSAGE);

		ProductModifier.getProductList(searchTerm);

		showProduct();

    }//GEN-LAST:event_btnProductSearchActionPerformed

    private void tbProductMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbProductMousePressed
		int pos = tbProduct.getSelectedRow();
		productIndex = pos;
		txtProductName.setText(productData.get(pos).getProductName());
		cmbProductCategory.setSelectedItem(productData.get(pos).getCatID());
		txtProductPrice.setText(productData.get(pos).getPrice() + "");
		txtProductDescription.setText(productData.get(pos).getDescription());
    }//GEN-LAST:event_tbProductMousePressed

	private void tbCategoryMousePressed(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_tbCategoryMousePressed
		int pos = tbCategory.getSelectedRow();
		catIndex = pos;
		txtCategory.setText(catData.get(pos).getCatName());
	}// GEN-LAST:event_tbCategoryMousePressed

	private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnAddActionPerformed
		String catName = txtCategory.getText();
		Category insertData = new Category(catName);
		CategoryModifier.addCategory(insertData);

		showCategory();
	}// GEN-LAST:event_btnAddActionPerformed

	private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnEditActionPerformed
		if (catIndex != -1) {
			String a = txtCategory.getText();
			Category edit = new Category(catIndex, a);

			CategoryModifier.editCategory(edit);
			catIndex = -1;

			showCategory();
		} else {
			return;
		}
	}// GEN-LAST:event_btnEditActionPerformed

	private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnDeleteActionPerformed
		if (catIndex != -1) {
			int opt = JOptionPane.showConfirmDialog(rootPane, "Bạn có muốn xóa đối tượng này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
			if (opt != 1) {
				CategoryModifier.deleteCategory(catIndex);

				catIndex = -1;
			} else {
				return;
			}
			System.out.println(opt);
		} else {
			JOptionPane.showMessageDialog(rootPane, "Chưa chọn đối tượng muốn xóa", "Thông báo", JOptionPane.WARNING_MESSAGE);
		}
	}// GEN-LAST:event_btnDeleteActionPerformed

